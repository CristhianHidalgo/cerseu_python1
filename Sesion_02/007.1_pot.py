"""2da FORMA"""
""""Usando la palabra reservada 'pow' podemos también 
    elevar a una cierta potencia un número"""

"""Nos va a pedir 2 valores, el primero es la base y 
   el segundo valor es la potencia a la que se eleva"""

potencia_2 = pow(20, 4)
potencia_3 = pow(5, 5)

print(f"El valor de mi pontencia_2 es: {potencia_2}"
      f"\nEl valor de mi pontencia_3 es: {potencia_3}")
