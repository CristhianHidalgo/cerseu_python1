
"""Listas en Python"""

"""Listas pop(): Quita un elemento de una posición dada a pop"""

paises = ["Perú", "Francia", "Colombia", "Argentina"]

print("Los valores iniciales en mi lista son: {}".format(paises))

paises.pop()

print("Los valores actualizados en mi lista son: {}".format(paises))

paises.pop(0)

print("Los valores actualizados en mi lista son: {}".format(paises))
