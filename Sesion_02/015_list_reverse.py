"""Listas en Python"""

"""Listas reverse(): Revierte el orden de los elementos de una lista"""

persona = ["Cristhian", "Hidalgo", 20, "22/07/2003"]

print("Los datos iniciales de mi lista son: {}".format(persona))

persona.reverse()
print("Los valores actualizados de mi lista son: {}".format(persona))

print(persona)
