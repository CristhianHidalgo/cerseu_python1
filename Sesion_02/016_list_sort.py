"""Listas en Python"""

"""Listas sort(): Ordena los elementos de una lista"""

var_1 = ["SQLServer", "RDS", "mySQL", "postgreSQL"]
var_2 = [50, 75, 1560, 400, 3, 4000]

var_1.sort()
var_2.sort()

print("Los valores de mi lista de var_1 ordenada es: {}".format(var_1) )
print("Los valores de mi lista de var_2 ordenada es: {}".format(var_2) )